    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
            Registry registry = LocateRegistry.getRegistry(2000);
            AccountInter accdao = (AccountInter) registry.lookup("ServerAccount");
            Registry reg = LocateRegistry.getRegistry(2005);
            MethodsInter io = (MethodsInter) reg.lookup("ServerMethods");
            Account acc = new Account();
            for(int i = 0;i <=100;i++){
                try {
                    Thread.sleep(10);
                } catch (InterruptedException ex) {
                    Logger.getLogger(AccountView.class.getName()).log(Level.SEVERE, null, ex);
                }
                pr1.setValue(i);
            }
            if(ptxt.getText().equals(cptxt.getText())){
                acc.setFirstname(fnametxt.getText());
                acc.setLastname(lnametxt.getText());
                acc.setUsername(unametxt.getText());
                acc.setPassword(io.encoding(ptxt.getText()));
                acc.setEmail(emailtxt.getText());
                acc.setPhonenumber(fnumtxt.getText());
                accdao.createAccount(acc);
                JOptionPane.showMessageDialog(this, "Account created!! ");
            }
            else
                JOptionPane.showMessageDialog(this, "The Password not match ");
        } catch (RemoteException | NotBoundException ex) {
            Logger.getLogger(AccountView.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

