    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        try {
            String name = jTextField2.getText().replaceAll("( +)", " ").trim();
            Registry registry = LocateRegistry.getRegistry(2004);
            ClientInter cdao = (ClientInter) registry.lookup("ServerClient");
            List<ClientView> clients = cdao.findByName(name);
            if(clients!=null){
                for(Iterator it= clients.iterator();it.hasNext();){
                    ClientView c = (ClientView) it.next();
                    cid.setText(c.getRegno());
                    cname.setText(c.getFirstname()+" "+c.getLastname());
                    ImageIcon i = new ImageIcon(c.getPhoto());
                    Image image = i.getImage().getScaledInstance(cphoto.getHeight(), cphoto.getWidth(), Image.SCALE_SMOOTH);
                    cphoto.setIcon(new ImageIcon(image));
                }
            }
        } catch (RemoteException ex) {
            Logger.getLogger(operationView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(operationView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTextField2KeyReleased

