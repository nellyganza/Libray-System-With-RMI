    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        try {
            Registry registry = LocateRegistry.getRegistry(2000);
            AccountInter adao = (AccountInter) registry.lookup("ServerAccount");
            Registry reg = LocateRegistry.getRegistry(2005);
            MethodsInter io = (MethodsInter) reg.lookup("ServerMethods");
            String username = uname.getText();
            String password = ptxt.getText();
            
            String a = io.deencoding(adao.findByUsername(username));
            if(a!=null){
                if(a.equals(password)){
                    this.hide();
                    mainView m = new mainView();
                    m.show();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Invalid Password");
                }
            }else
            {
                JOptionPane.showMessageDialog(this, "Invalid Username");   
            }
        } catch (RemoteException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginbtnActionPerformed

