    public void addData(){
        try {
            Registry registry = LocateRegistry.getRegistry(2001);
            BookCategoryInter bcdao = (BookCategoryInter) registry.lookup("ServerBookCategory");
            List<BookcategoryView> catnames  = bcdao.getDataIntoBookViewCatCmb();
            cmb.removeAllItems();
            for(Object b:catnames){
                cmb.addItem(b.toString());
            }
        } catch (RemoteException ex) {
            Logger.getLogger(ReportsView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(ReportsView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void reportByCategory(String category){
        try {
            Methods m = new Methods();
            Object[] colnames = {"OPERATION NUM.","CLIENT NAME","BOOK NAME","TAKEN/RETURNED DATE","OPERATION CAT.","STATUS"};
            DefaultTableModel tm = (DefaultTableModel) table.getModel();
            tm.setRowCount(0);
            tm.setColumnIdentifiers(colnames);
            Registry registry = LocateRegistry.getRegistry(2003);
            CheckInOutInter ckdao = (CheckInOutInter) registry.lookup("ServerCheckInOut");
            List<Object[]> operations = ckdao.getReportOfDataCategory(cmb.getSelectedItem().toString(), checkin.isSelected()?"CHECK IN":"CHECK OUT");
            for(Object[] c:operations){
                String[] dat = {c[0].toString(),m.getNameOfClient(c[1].toString()).toString(),m.getName("title", "BookView", "bookid",c[2].toString()),new SimpleDateFormat("dd-MM-yyyy").format((Date) c[3]),c[4].toString(),c[5].toString()};
                
                if("All".equals(paneres) || paneres==null){
                    tm.addRow(dat);
                }
                else if(paneres.equals(c[5].toString())){
                    tm.addRow(dat);
                }
                
                
            }
            table.setModel(tm);
        } catch (RemoteException ex) {
            Logger.getLogger(ReportsView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(ReportsView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void reportByBookTitle(String title){
        try {
            Methods m = new Methods();
            Registry registry = LocateRegistry.getRegistry(2003);
            CheckInOutInter ckdao = (CheckInOutInter) registry.lookup("ServerCheckInOut");
            List<CheckinoutView> operations = ckdao.getReportOfData("bookView", m.getId("BookView", "title", text.getText(), "bookid"), checkin.isSelected()?"CHECK IN":"CHECK OUT");
            DefaultTableModel tm = (DefaultTableModel) table.getModel();
            tm.setRowCount(0);
            String[] colnames = {"OPERATION NUM.","CLIENT NAME","BOOK NAME","TAKEN/RETURNED DATE","OPERATION CAT.","STATUS"};
            tm.setColumnIdentifiers(colnames);
            for(CheckinoutView c:operations){
                String obj[] ={c.getOpnumber(),m.getNameOfClient(c.getClientView().getRegno()).toString(),m.getName("title", "BookView", "bookid",c.getBookView().getBookid()),new SimpleDateFormat("dd-MM-yyyy").format(c.getDatetime()),c.getOperationcategory(),c.getStatus()};
                if("All".equals(paneres) || paneres==null){
                    tm.addRow(obj);
                }
                else if(paneres.equals(c.getStatus())){
                    tm.addRow(obj);
                }
            }
            table.setModel(tm);
        } catch (RemoteException ex) {
            Logger.getLogger(ReportsView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(ReportsView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
