    public void populateClientView(){
        try {
            Registry registry = LocateRegistry.getRegistry(2004);
            ClientInter cdao = (ClientInter) registry.lookup("ServerClient");
            List<ClientView> clients = cdao.getClient();
            String[] colnames = {"REG NO","FIRST NAME","LAST NAME","EMAIL ADDRESS","PHONE NUMBER","CLIENT CATEGORY","PHOTO"};
            DefaultTableModel model = (DefaultTableModel) clienttbl.getModel();
            model.setRowCount(0);
            model.setColumnIdentifiers(colnames);
            clienttbl.getColumn("PHOTO").setCellRenderer(new myTableCellRenderer());
            for(Iterator it = clients.iterator();it.hasNext();){
                ClientView c = (ClientView) it.next();
                JLabel lb = new JLabel();
                ImageIcon imgiconn = new ImageIcon(c.getPhoto());
                Image img = imgiconn.getImage().getScaledInstance(70, 70, Image.SCALE_DEFAULT);
                lb.setIcon(new ImageIcon(img));
                Object[] cl = {c.getRegno(),c.getFirstname(),c.getLastname(),c.getEmailaddress(),c.getPhonenumber(),c.getClientcategory(),lb};
                model.addRow(cl);
            }
            clienttbl.setModel(model);
        } catch (RemoteException ex) {
            Logger.getLogger(clientView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(clientView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    class myTableCellRenderer implements TableCellRenderer{


        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
           
            TableColumn tc = clienttbl.getColumn("PHOTO");
            tc.setMaxWidth(70);
            table.setRowHeight(70);
            return (Component) value;
           
        }
         
    }
