    private void uploadbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadbtnActionPerformed
       JFileChooser file = new JFileChooser();
        FileNameExtensionFilter fnef  = new FileNameExtensionFilter("Extension", "jpeg","png");
        file.addChoosableFileFilter(fnef);
        if(file.showOpenDialog(this)==JFileChooser.APPROVE_OPTION){
            File myimage = file.getSelectedFile();
            path = myimage.getAbsolutePath();
            imgicon = new ImageIcon(path);
            Image image = imgicon.getImage().getScaledInstance(photolbl.getWidth(), photolbl.getHeight(), Image.SCALE_DEFAULT);
            photolbl.setIcon(new ImageIcon(image));
        }
    }//GEN-LAST:event_uploadbtnActionPerformed

