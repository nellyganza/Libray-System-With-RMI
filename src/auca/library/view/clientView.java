    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        try {
            ClientView c = new ClientView();
            c.setRegno(regnotxt.getText());
            c.setFirstname(fnametxt.getText());
            c.setLastname(lnametxt.getText());
            c.setEmailaddress(addresstxt.getText());
            c.setPhonenumber(phonenumbertxt.getText());
            c.setClientcategory(ccattxt.getSelectedItem().toString());
            c.setPhoto(path);
            Registry registry = LocateRegistry.getRegistry(2004);
            ClientInter cdao = (ClientInter) registry.lookup("ServerClient");
            cdao.saveClient(c);
            populateClientView();
            JOptionPane.showMessageDialog(this, "data Saved Successfully !!!!");
        } catch (RemoteException ex) {
            Logger.getLogger(clientView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(clientView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_savebtnActionPerformed

